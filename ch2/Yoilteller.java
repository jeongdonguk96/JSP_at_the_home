package com.fastcampus.ch2;

import java.io.IOException;
import java.io.PrintWriter;
import java.util.Calendar;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;

@Controller
public class Yoilteller { // 년월일을 입력하면 요일을 알려주는 프로그램

	@RequestMapping("/getYoil")
	public void main(HttpServletRequest request, HttpServletResponse response) throws IOException {
		// 1. 입력
		String year = request.getParameter("year"); // 파라미터에 적은 "year"은 name. 실제 value는 파라미터로 입력
		String month = request.getParameter("month");
		String day = request.getParameter("day");

		int yyyy = Integer.parseInt(year);
		int mm = Integer.parseInt(month);
		int dd = Integer.parseInt(day);

		// 2. 처리 = Controller
		Calendar cal = Calendar.getInstance();
		cal.set(yyyy, mm -1, dd);
		
		int dayOfWeek = cal.get(Calendar.DAY_OF_WEEK); // DAY_OF_WEEK : 1~7까지의 숫자로 일~토 요일을 변환하는 매서드
		char yoil = "일월화수목금토".charAt(dayOfWeek); // charAt : 뒤에 오는 숫자와 동일한 앞 문자열의 인덱스 지정
		
		// 3. 출력 = View
		response.setContentType("text/html"); // 응답의 형식을 text중에 html로 지정 
		response.setCharacterEncoding("utf-8"); // 응답의 인코딩을 utf-8로 지정
		PrintWriter out = response.getWriter(); // response객체에서 브라우저로의 출력 스트림을 얻는다.
		out.println("<html>");
		out.println("<head>");
		out.println("</head>");
		out.println("<body>");
		out.println(year + "년 " +  month + "월 " +  day + "일은");
		out.println(yoil + "요일입니다.");
		out.println("</body>");
		out.println("</html>");
	}

}
